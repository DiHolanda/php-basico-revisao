<?php 
	
	$a = 100;
	$b = 80;

	// compara se valores são iguais
	if($a == $b){
		echo $a . " é igual a " . $b;
	}
	else {
		echo $a . " é diferente de " . $b;
	}

	echo "\n";

	// compara se valores são diferentes
	if($a != $b){
		echo $a . " é diferente de " . $b;
	}
	else {
		
		echo $a . " é igual a " . $b;
	}

	echo "\n";

	// compara se um valor é maior que outro
	if($a > $b){
		echo $a . " é maior que " . $b;
	}
	else {
		echo $a . " não é maior que " . $b;
	}
 ?>